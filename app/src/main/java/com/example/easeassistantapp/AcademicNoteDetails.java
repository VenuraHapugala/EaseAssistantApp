package com.example.easeassistantapp;

public class AcademicNoteDetails {
    private String note;
    private String title;

    public AcademicNoteDetails(String note, String title) {
        this.note = note;
        this.title = title;
    }

    public String getNote() {
        return note;
    }

    public String getTitle() {
        return title;
    }
}

